package com.gqt.Association;

public class OperatingSystem 
{
	private String name;
	private int size;
	public OperatingSystem(String name, int size) {
		
		this.name = name;
		this.size = size;
	}
	public String getName() {
		return name;
	}
	public int getSize() {
		return size;
	}
	
	
}
