package com.gqt.Association1;

public class Heart
{
	private int heartbeat;
	private int weight;
	public Heart(int heartbeat, int weight) {
		
		this.heartbeat = heartbeat;
		this.weight = weight;
	}
	

	public int getHeartbeat() {
		return heartbeat;
	}
	public int getWeight() {
		return weight;
	}
}
	
