package com.gqt.Association1;

public class Brain
{
	private String colour;
	private int weight;
	public Brain(String colour, int weight) {
		
		this.colour = colour;
		this.weight = weight;
	}
	public String getColour() {
		return colour;
	}
	public int getWeight() {
		return weight;
	}
	
}
