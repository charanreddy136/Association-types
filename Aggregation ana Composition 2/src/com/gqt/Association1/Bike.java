package com.gqt.Association1;

public class Bike 
{
	private String brand;
	private int mileage;
	public Bike(String brand, int mileage) {
		
		this.brand = brand;
		this.mileage = mileage;
	}
	public String getBrand() {
		return brand;
	}
	public int getMileage() {
		return mileage;
	}
	
}
